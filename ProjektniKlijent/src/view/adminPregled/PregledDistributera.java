    public void popuniTabelu(){
        try {
            TObjekat posalji = new TObjekat(null, "listaDistributera");
            KomunikacijaKlijent.vratiObjekat().posalji(posalji);
            TObjekat odgovor = KomunikacijaKlijent.vratiObjekat().procitaj();
            
            List<Distributer> distributeri = (List<Distributer>) odgovor.getObjekat();
            ModelTableDistributeri mtd = new ModelTableDistributeri(distributeri);
            jTable1.setModel(mtd);
        } catch (IOException ex) {
            Logger.getLogger(PregledDistributera.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PregledDistributera.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
